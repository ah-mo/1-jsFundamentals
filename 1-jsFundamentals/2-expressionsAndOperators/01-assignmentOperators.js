// Assignment Operators

//Assignment Operator
let r = 5;

//Addition operator
r = r + 1;
r += 1;
console.log(r);

//Subraction Operator 

r = r - 1;
r -= 1;

//Multiplication Operator 
r = r * 1;
r *= 1;

//Division Operator
r = r / 1;
r /= 1;

//Remainder (or Modulus) Operator
r = r % 1;
r %= 1;

//Exponential Operator 
r = r ** 1;
r **= 1;

// Remainder vs Division
console.log(10/2);
console.log(9/2);
console.log(9%2);
